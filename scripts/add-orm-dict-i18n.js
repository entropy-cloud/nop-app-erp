function esc(val) {
  if (val == null) return 'null';
  val = val.replace(/&/g, '&amp;').replace(/</g, '&lt;');
  return val.includes("'") ? `"${val}"` : `'${val}'`;
}

function translate(label) {
  const trimmed = label.trim();
  if (TRANS[trimmed]) return TRANS[trimmed];
  const parenMatch = trimmed.match(/^(.+?)\s*[（(].+?[）)]$/);
  if (parenMatch && TRANS[parenMatch[1]]) return TRANS[parenMatch[1]];
  // fallback: integer values stay as-is, others use original
  if (/^\d+$/.test(trimmed)) return null;
  return null; // unknown
}

function addDictI18n(xml) {
  // Add i18n-en:label to <dict> elements (skip if already present)
  xml = xml.replace(/(<dict\s+)([\s\S]*?)(\/?>)/g, (match, open, attrs, close) => {
    if (match.includes('i18n-en:label')) return match;
    const labelMatch = attrs.match(/\blabel=["']([^"']+)["']/);
    if (!labelMatch) return match;
    const en = translate(labelMatch[1]);
    if (!en) return match;
    // Insert after the last existing attribute (before > or />)
    return open + attrs + ` i18n-en:label=${esc(en)}` + close;
  });

  // Add i18n-en:label to <option> elements
  xml = xml.replace(/(<option\s+)([\s\S]*?)(\/?>)/g, (match, open, attrs, close) => {
    if (match.includes('i18n-en:label')) return match;
    const labelMatch = attrs.match(/\blabel=["']([^"']+)["']/);
    if (!labelMatch) return match;
    const en = translate(labelMatch[1]);
    if (!en) return match;
    return open + attrs + ` i18n-en:label=${esc(en)}` + close;
  });

  return xml;
}

function main() {
  const filterModule = process.argv[2];
  const entries = fs.readdirSync(ROOT).filter(
    e => e.startsWith('module-') && fs.statSync(path.join(ROOT, e)).isDirectory()
  );

  for (const entry of entries) {
    const modName = entry.replace('module-', '');
    if (filterModule && entry !== filterModule && modName !== filterModule) continue;
    const modelDir = path.join(ROOT, entry, 'model');
    if (!fs.existsSync(modelDir)) continue;

    for (const f of fs.readdirSync(modelDir).filter(f => f.endsWith('.orm.xml'))) {
      const fp = path.join(modelDir, f);
      console.log(`Adding i18n-en:label to dicts in: ${entry}/${f} ...`);
      let xml = fs.readFileSync(fp, 'utf-8');
      const original = xml;
      xml = addDictI18n(xml);
      if (xml !== original) {
        fs.writeFileSync(fp, xml, 'utf-8');
      } else {
        console.log('  (no changes)');
      }
    }
  }
  console.log('\nDone.');
}

main();
